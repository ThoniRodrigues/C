// Como ler um vetor com o loop for
// cria um vetor de inteiros de 5 posições

#include <stdio.h>
#include <stdlib.h>

int main()
{
// Cria um vetor de inteiros com 5 posições
int vetor[5];
int i = 0;

// loop for para ler os valores
for (i=0; i<5; i++)
{
	printf("\n Digite o valor %d: ", i);
	scanf("%d", &vetor[i]);
}
	

//loop for 
}